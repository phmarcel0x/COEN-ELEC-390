package com.example.myapptest.Controllers;

public class Firebase_Data_Helper {

    // Declaration of the Variables
    String name, email, username, password;

    // Constructor with Arguments for the Firebase Database
    public Firebase_Data_Helper(String name, String email, String username, String password) {
        this.name = name;
        this.email = email;
        this.username = username;
        this.password = password;
    }

    // Getters and Setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public Firebase_Data_Helper() {

    }
}
